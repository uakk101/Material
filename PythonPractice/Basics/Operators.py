

#------------------Operators in Python------------------#

x = 10
y = 3

addition = x + y
subtraction = x - y
multiplication = x * y
division = x / y
modulus = x % y
exponentiation = x ** y

print(addition)        # Output: 13
print(subtraction)     # Output: 7
print(multiplication)  # Output: 30
print(division)        # Output: 3.333333
print(modulus)         # Output: 1
print(exponentiation)  # Output: 1000

#-------------------Comparison Operators-------------------#

x = 5
y = 3

equal_to = x == y
not_equal_to = x != y
greater_than = x > y
less_than = x < y
greater_than_or_equal_to = x >= y
less_than_or_equal_to = x <= y

print(equal_to)                 # Output: False
print(not_equal_to)             # Output: True
print(greater_than)             # Output: True
print(less_than)                # Output: False
print(greater_than_or_equal_to)  # Output: True
print(less_than_or_equal_to)     # Output: False


#-=================Logical Operators==================#

x = True
y = False

logical_and = x and y
logical_or = x or y
logical_not_x = not x
logical_not_y = not y

print(logical_and)    # Output: False
print(logical_or)     # Output: True
print(logical_not_x)  # Output: False
print(logical_not_y)  # Output: True


#------------Assignment Operators----------------#

x = 5
y = 3

x += y  # equivalent to x = x + y
y *= 2  # equivalent to y = y * 2

print(x)  # Output: 8
print(y)  # Output: 6












